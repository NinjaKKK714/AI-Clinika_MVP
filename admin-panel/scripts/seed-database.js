        console.log(`   - –°–ø–µ—Ü–∏–∞–ª–∏—Å—Ç—ã: ${specialistsResult.rows[0].count}`);
        console.log(`   - –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–∏: ${usersResult.rows[0].count}`);
        
